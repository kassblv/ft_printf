#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*new;
	int		i;

	if ((new = (char *)malloc((ft_strlen(src) + 1) * sizeof(char))) == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
